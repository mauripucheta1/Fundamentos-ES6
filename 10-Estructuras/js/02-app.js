const puntaje2 = 100;

// Igual a (==)
if (puntaje2 == "100") {
    console.log('Es igual');
} else {
    console.log('No es igual');
};

// Igual a (==)
if (puntaje2 == 100) {
    console.log('Es igual');
} else {
    console.log('No es igual');
};

// Extrictamente igual a (===)
if (puntaje2 === "100") {
    console.log('Es igual');
} else {
    console.log('No es igual');
};

// Extrictamente igual a (===)
if (puntaje2 === 100) {
    console.log('Es igual');
} else {
    console.log('No es igual');
};

// Diferente a (!==)
if (puntaje2 !== 100) {
    console.log('No es igual');
} else {
    console.log('Es igual');
};