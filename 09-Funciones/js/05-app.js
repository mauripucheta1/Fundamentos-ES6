// Función para restar
function restar (b, c) {

    console.log('El resultao de la resta es:', b - c);

};

// Llamada a la función
restar(20, 10);

// Función para saludar
function saludar (nombre, apellido) {

    console.log(`¡Hola ${nombre} ${apellido}. ¿Cómo estás?`);

};

saludar('Mauricio', 'Pucheta');

