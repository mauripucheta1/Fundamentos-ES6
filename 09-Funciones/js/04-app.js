const numero1 = 20;
const numero2 = 'hola';

// Función
console.log(parseFloat(numero1));

// Método
console.log(numero2.toUpperCase());

// Aclaraciones: Son similares pero las funciones existen de forma independiente, mientras que un método es una función asociada a un objeto (se vuelve método porque está como propiedad de un objeto).