const booleanTrue = true;
const booleanFalse = false;

// Comparación si son iguales
console.log('¿El primer booleano es igual al segundo?:', booleanTrue === booleanFalse);

// Comparación si son distintos
console.log('¿El primer booleano es distinto al segundo?:', booleanTrue !== booleanFalse);

// Comparación contra valores
console.log('¿El primer booleano es igual a "true"?:', booleanTrue === true);
console.log('¿El segundo booleano es distinto a "false"?:', booleanFalse !== false);